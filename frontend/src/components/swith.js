function chainTOSwitch(val){
    var answer = "";

    switch(val){
        case 1:
            answer = "nadib";
            break;
        case 2:
            answer = "Rana";
            break;
        case 3:
            answer = "Number 3"
            break;
    }
    return answer;

}
